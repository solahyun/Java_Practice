
public class Ex02 {

	public static void main(String[] args) {
		
		// 숫자 + 숫자 = 숫자
		int num1 = 10;
		int num2 = 7;
		
		// 출력 명령 자동완성 : syso + Ctrl + space bar
		System.out.println(num1 + num2);
		
		// 숫자 +(연결) 문자 = 문자
		int num3 = 2;
		char num4 = '1';  // 아스키코드 49
		System.out.println(num3 + num4); // 51
		
		String num5 = "1";
		System.out.println(num3 + num5);
	}

}
