
public class Ex02 {
	
	// 컴파일 : 클래스 파일을 실행시키는 작업의 말
	public static void main(String[] args) {
		
		// 1. 변수 선언
		int a = 0;
		System.out.println(a);
		
		a = 5;
		System.out.println(a);
		
		// final은 변하지 않는 데이터를 담기 위한 키워드
		final int b = 10;
//		b = 5;  // 상수는 재할당 불가!
		System.out.println(b);
		
		// class 실행시키는 단축키 : Ctrl + F11
		
		
	}

}
