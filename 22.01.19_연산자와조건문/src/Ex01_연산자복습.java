
public class Ex01_연산자복습 {

	public static void main(String[] args) {
	
		// 산술연산자
		// 1.기본타입 2.참조타입
		// 기본 타입 8가지
		// 정수형 - byte, short, int, long
		// 실수형 - float, double
		// 논리형 - boolean
		// 문자형, 정수형 - char
		char txt = 'A'; //문자 타입, 정수 타입
		System.out.println(txt);
		System.out.println((int)txt);
		System.out.println(txt+1);	
		System.out.println((char)(txt+1));
	
	}

}
