
public class Ex05_비교연산자 {

	public static void main(String[] args) {
		// 좌항과 우항의 값을 비교 : 결과값은 항상 boolean으로 나옴
		int a = 3;
		int b = 10;
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<b); 
		System.out.println(a<=b); //*
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
	}

}
